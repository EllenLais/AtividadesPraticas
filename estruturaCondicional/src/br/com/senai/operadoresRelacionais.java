package br.com.senai;

public class operadoresRelacionais {
    public static void main(String[] args) {
        System.out.println("10 == 3: " + (10 == 3));
        System.out.println("10 != 3: " + (10 != 3));
        System.out.println("10 > 3: " + (10 > 3));
        System.out.println("10 < 3: " + (10 < 3));
        System.out.println("10 >= 3: " + (10 >= 3));
        System.out.println("10 <= 3: " + (10 <= 3));

        int idade = 18;
        System.out.println("Idade == 18: " + (idade == 18));
        System.out.println("Idade != 18: " + (idade != 18));
        System.out.println("Idade > 18: " + (idade > 18));
        System.out.println("Idade < 18: " + (idade < 18));
        System.out.println("Idade >= 18: " + (idade >= 18));
        System.out.println("Idade <= 18: " + (idade <= 18));
    }
}
